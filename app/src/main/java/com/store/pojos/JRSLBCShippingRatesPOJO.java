package com.store.pojos;

public class JRSLBCShippingRatesPOJO {

    private String qty;
    private String lbcRate;
    private String jrsRate;

    public String getQty() {
        return qty;
    }

    public void setQty(String qty) {
        this.qty = qty;
    }

    public String getLbcRate() {
        return lbcRate;
    }

    public void setLbcRate(String lbcRate) {
        this.lbcRate = lbcRate;
    }

    public String getJrsRate() {
        return jrsRate;
    }

    public void setJrsRate(String jrsRate) {
        this.jrsRate = jrsRate;
    }
}
